//lastindexOf(). Como descobrir o último índice de um valor de string (para quando temos mais de um valor repetido)

const str = 'santos sempre santos'
console.log(str.indexOf('santos')) // posição 0 
console.log(str.lastIndexOf('santos')) //posição 14